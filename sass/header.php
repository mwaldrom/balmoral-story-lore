                $breadcrumbs = array_filter(explode('/', parse_url($_SERVER['REQUEST_URI'], PHP_URL_PATH)));
                $breadcrumb .= '<li><i class="fa fa-home"></i><a class="grey-text text-darken-1" href="' . $root_domain . '" title="Home Page"><span>' . $home . '</span></a></li>';
                foreach ($breadcrumbs as $crumb) {
                    $link = ucwords(str_replace(array(".php","-","_"), array(""," "," "), $crumb));
                    $root_domain .=  $crumb . '/';
                    $breadcrumb .= '<li><a class="grey-text text-darken-1" href="'. $root_domain .'" title="'.$page_title.'"><span>' . $link . '</span></a></li>';
                }
                $breadcrumb .= '</ol>';
                $breadcrumb .= '</div>';
                $breadcrumb .= '</div>';
                return $breadcrumb;
            }
