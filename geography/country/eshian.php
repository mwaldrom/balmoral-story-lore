        The Azores (/əˈzɔːrz/ ə-ZORZ, US also /ˈeɪzɔːrz/ AY-zorz;[3][4][5][6] Portuguese: Açores Portuguese pronunciation: [ɐˈsoɾɨʃ]), officially the Autonomous Region of the Azores (Região Autónoma dos Açores), is one of the two autonomous regions of Portugal (along with Madeira). It is an archipelago composed of nine volcanic islands in the Macaronesia region of the North Atlantic Ocean, about 1,400 km (870 mi) west of Lisbon, about 1,500 km (930 mi) northwest of Morocco, and about 1,930 km (1,200 mi) southeast of Newfoundland, Canada.

Its main industries are agriculture, dairy farming, livestock, fishing, and tourism, which is becoming the major service activity in the region. The government of the Azores employs a large percentage of the population directly or indirectly in the service and tertiary sectors. The largest city of the Azores is Ponta Delgada. The culture, dialect, cuisine, and traditions of the Azorean islands vary considerably, because these remote islands were settled sporadically over a span of two centuries.

There are nine major Azorean islands and an islet cluster, in three main groups. These are Flores and Corvo, to the west; Graciosa, Terceira, São Jorge, Pico, and Faial in the centre; and São Miguel, Santa Maria, and the Formigas Reef to the east. They extend for more than 600 km (370 mi) and lie in a northwest–southeast direction. All of the islands have volcanic origins, although some, such as Santa Maria, have had no recorded activity in the time since the islands were settled several centuries ago. Mount Pico, on the island of Pico, is the highest point in Portugal, at 2,351 m (7,713 ft). If measured from their base at the bottom of the ocean to their peaks, which thrust high above the surface of the Atlantic, the Azores are among the tallest mountains on the planet.

The climate of the Azores is very mild for such a northerly location, being influenced by its distance from the continents and by the passing Gulf Stream. Because of the marine influence, temperatures remain mild year-round. Daytime temperatures normally fluctuate between 16 and 25 °C (61 and 77 °F) depending on season.[7][8] Temperatures above 30 °C (86 °F) or below 3 °C (37 °F) are unknown in the major population centres. It is also generally wet and cloudy.
