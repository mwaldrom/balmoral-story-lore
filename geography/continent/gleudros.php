            Antarctica (/ænˈtɑːrktɪkə/ (audio speaker iconlisten) or /ænˈtɑːrtɪkə/)[note 1] is Earth's southernmost continent. Situated almost entirely south of the Antarctic Circle and surrounded by the Southern Ocean, it contains the geographic South Pole. Antarctica is the fifth-largest continent, being nearly twice the size of Australia, and has an area of 14,200,000 km2 (5,500,000 sq mi). During the summer months 5,000 people reside at research stations, a figure that drops to around 1,000 in the winter. Most of Antarctica is covered by ice, with an average thickness of 1.9 km (1.2 mi).

